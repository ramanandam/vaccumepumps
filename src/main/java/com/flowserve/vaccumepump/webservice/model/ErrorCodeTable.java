package com.flowserve.vaccumepump.webservice.model;

public class ErrorCodeTable {

	private int errorcode;
	private String errorType;
	private String errorDescriptionInGerman;
	private String errorDescriptionInEnglish;
	private String additionalParametersGerman;
	private String additionalParametersEnglish;
	private String explanationsAndPossibleRemediesGerman;
	private String explanationsAndPossibleRemediesEnglish;
	public int getErrorcode() {
		return errorcode;
	}
	public void setErrorcode(int errorcode) {
		this.errorcode = errorcode;
	}
	public String getErrorType() {
		return errorType;
	}
	public void setErrorType(String errorType) {
		this.errorType = errorType;
	}
	public String getErrorDescriptionInGerman() {
		return errorDescriptionInGerman;
	}
	public void setErrorDescriptionInGerman(String errorDescriptionInGerman) {
		this.errorDescriptionInGerman = errorDescriptionInGerman;
	}
	public String getErrorDescriptionInEnglish() {
		return errorDescriptionInEnglish;
	}
	public void setErrorDescriptionInEnglish(String errorDescriptionInEnglish) {
		this.errorDescriptionInEnglish = errorDescriptionInEnglish;
	}
	public String getAdditionalParametersGerman() {
		return additionalParametersGerman;
	}
	public void setAdditionalParametersGerman(String additionalParametersGerman) {
		this.additionalParametersGerman = additionalParametersGerman;
	}
	public String getAdditionalParametersEnglish() {
		return additionalParametersEnglish;
	}
	public void setAdditionalParametersEnglish(String additionalParametersEnglish) {
		this.additionalParametersEnglish = additionalParametersEnglish;
	}
	public String getExplanationsAndPossibleRemediesGerman() {
		return explanationsAndPossibleRemediesGerman;
	}
	public void setExplanationsAndPossibleRemediesGerman(String explanationsAndPossibleRemediesGerman) {
		this.explanationsAndPossibleRemediesGerman = explanationsAndPossibleRemediesGerman;
	}
	public String getExplanationsAndPossibleRemediesEnglish() {
		return explanationsAndPossibleRemediesEnglish;
	}
	public void setExplanationsAndPossibleRemediesEnglish(String explanationsAndPossibleRemediesEnglish) {
		this.explanationsAndPossibleRemediesEnglish = explanationsAndPossibleRemediesEnglish;
	}
	
	
	
}
